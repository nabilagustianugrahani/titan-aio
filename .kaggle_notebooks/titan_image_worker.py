    "log(\"Installing torch + diffusers...\")\n",
    "subprocess.run(\"pip install -q torch torchvision torchaudio --index-url https://download.pytorch.org/whl/cu124\", shell=True, timeout=300)\n",
    "subprocess.run(\"pip install -q diffusers transformers accelerate sentencepiece redis boto3 pillow\", shell=True, timeout=180)\n",
    "log(\"Dependencies done\")\n",
    "\n",
    "# \u2500\u2500 2. Check GPU \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n",
    "gpu = subprocess.run(\"nvidia-smi --query-gpu=name,memory.total --format=csv,noheader\", shell=True, capture_output=True, text=True)\n",
    "log(f\"GPU: {gpu.stdout.strip() if gpu.returncode==0 else 'NONE (CPU mode)'}\")\n",
    "\n",
    "# \u2500\u2500 3. Load Model \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n",
    "log(\"Loading FLUX.1-schnell...\")\n",
    "from diffusers import FluxPipeline\n",
    "import torch\n",
    "pipe = FluxPipeline.from_pretrained(\"black-forest-labs/FLUX.1-schnell\", torch_dtype=torch.bfloat16)\n",
    "pipe.to(\"cuda\" if torch.cuda.is_available() else \"cpu\")\n",
    "pipe.enable_model_cpu_offload()\n",
    "log(\"FLUX model loaded!\")\n",
    "\n",
    "# \u2500\u2500 4. Connect Redis \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n",
    "import redis\n",
    "\n",
    "REDIS_URL = os.environ.get(\"TITAN_REDIS_URL\", \"redis://localhost:6379/0\")\n",
    "S3_BUCKET = os.environ.get(\"TITAN_S3_BUCKET\", \"titan-assets\")\n",
    "r = redis.Redis.from_url(REDIS_URL, decode_responses=True)\n",
    "\n",
    "# \u2500\u2500 5. Upload to S3 \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n",
    "def upload_to_s3(file_path: str, key: str) -> str:\n",
    "    import boto3\n",
    "    s3 = boto3.client(\n",
    "        \"s3\",\n",
    "        endpoint_url=os.environ.get(\"TITAN_S3_ENDPOINT\", \"http://localhost:9000\"),\n",
    "        aws_access_key_id=os.environ.get(\"TITAN_S3_ACCESS_KEY\", \"minioadmin\"),\n",
    "        aws_secret_access_key=os.environ.get(\"TITAN_S3_SECRET_KEY\", \"minioadmin\"),\n",
    "    )\n",
    "    s3.upload_file(file_path, S3_BUCKET, key)\n",
    "    return f\"s3://{S3_BUCKET}/{key}\"\n",
    "\n",
    "# \u2500\u2500 6. Generate Function \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n",
    "def generate_image(prompt: str, model: str = \"flux-schnell\") -> dict:\n",
    "    log(f\"Generating: {prompt[:80]}...\")\n",
    "    t0 = time.time()\n",
    "\n",
    "    image = pipe(\n",
    "        prompt,\n",
    "        guidance_scale=3.5,\n",
    "        num_inference_steps=4 if \"schnell\" in model else 25,\n",
    "        width=1024,\n",
    "        height=1024,\n",
    "        generator=torch.Generator(\"cpu\").manual_seed(int(time.time()) % 2**31),\n",
    "    ).images[0]\n",
    "\n",
    "    file_id = uuid.uuid4().hex[:12]\n",
    "    local_path = f\"{OUTPUT_DIR}/{file_id}.png\"\n",
    "    image.save(local_path)\n",
    "\n",
    "    s3_url = upload_to_s3(local_path, f\"images/{file_id}.png\")\n",
    "    elapsed = int((time.time() - t0) * 1000)\n",
    "\n",
    "    log(f\"Done in {elapsed}ms: {s3_url}\")\n",
    "    return {\"image_url\": s3_url, \"model_used\": model, \"generation_time_ms\": elapsed}\n",
    "\n",
    "# \u2500\u2500 7. Polling Loop \u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\n",
    "log(f\"Worker {WORKER_ID} ready. Polling queue:image...\")\n",
    "\n",
    "while True:\n",
    "    job_data = r.blpop(\"queue:image\", timeout=30)\n",
    "    if job_data is None:\n",
    "        continue\n",
    "\n",
    "    try:\n",
    "        job = json.loads(job_data[1])\n",
    "        log(f\"Got job: {job['job_id']}\")\n",
    "        result = generate_image(job[\"payload\"].get(\"prompt\", \"\"), job[\"payload\"].get(\"model\", \"flux-schnell\"))\n",
    "        result[\"job_id\"] = job[\"job_id\"]\n",
    "        result[\"worker_id\"] = WORKER_ID\n",
    "        result[\"status\"] = \"completed\"\n",
    "        r.setex(f\"result:{job['job_id']}\", 3600, json.dumps(result))\n",
    "        log(f\"Job {job['job_id']} completed\")\n",
    "    except Exception as e:\n",
    "        log(f\"Job failed: {e}\")\n",
    "        r.setex(f\"result:{job['job_id']}\", 3600, json.dumps({\"status\": \"failed\", \"error\": str(e)}))\n",
